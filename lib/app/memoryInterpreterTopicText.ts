export const TOPIC_TAIL_RE =
  /(?:について(?:もっと)?(?:詳しく|くわしく)?(?:教えて|知りたい|説明して)?|について知って(?:いますか|ますか)?|について知っていますか|について|のことを(?:もっと)?(?:詳しく|くわしく)?(?:教えて|説明して)?|のことを知って(?:いますか|ますか)?|のことを知っていますか|を(?:もっと)?(?:詳しく|くわしく)?(?:教えて|説明して)?|を知って(?:いますか|ますか)?|を知っていますか|の弟子で一番有名なのは誰(?:か)?知っていますか|とは|って誰|って何|が終わった|が長い|が好き|ですか\??|は\??)(?:ですか|ますか|ください|下さい)?[。.!！?？\s]*$/u;

export function stripTopicTailText(text: string) {
  return text
    .replace(TOPIC_TAIL_RE, "")
    .replace(/のこと$/u, "")
    .replace(/[。.!！?？\s]+$/u, "")
    .trim();
}
